int VarGetSeq( )
/*----------------------------------------------------------------------------*/
{
	BOOL flag = TRUE;
	
	static int iSeq = 0;
	int ret;
	
	if (flag) {
		ret = iSeq;
		iSeq ++;
	}
	
	return ret;
}
